let data = [5,4,6,7];

//Push
console.log(data.push(2));//Return length 5 . Adds value to the end of the array
//pop
console.log(data.pop()); // Remove the last element. Here it is 2
//length
console.log(data.length)
//shift
console.log(data.shift());//removes 5 
console.log(data.unshift(2));//adds value in the front of the Array
console.log(data);
console.log(data.splice(1));//prints data from index position 1 
